/**
 * A dependency-free QR code generated by encoding the otpauth URI into a QR
 * matrix and drawing it on a <canvas> (byte mode, error correction level M,
 * versions 1..10). Falls back to a "scan unavailable" hint if the payload is
 * too large for the supported versions.
 */
function OtpauthQr({ data, size = 196 }: { data: string; size?: number }) {
    const canvasRef = useRef<HTMLCanvasElement | null>(null);

    // Compute the matrix during render so the failure state is derived, not set
    // synchronously inside the effect (which the lint rule forbids).
    const matrix = useMemo<boolean[][] | null>(() => {
        try {
            return buildQrMatrix(data);
        } catch {
            return null;
        }
    }, [data]);

    useEffect(() => {
        const canvas = canvasRef.current;
        if (!canvas || !matrix) return;
        const ctx = canvas.getContext('2d');
        if (!ctx) return;
        const n = matrix.length;
        const quiet = 4; // quiet-zone modules
        const total = n + quiet * 2;
        const scale = Math.max(1, Math.floor(size / total));
        const px = total * scale;
        canvas.width = px;
        canvas.height = px;
        // White background so the dark-theme page doesn't break scanning.
        ctx.fillStyle = '#ffffff';
        ctx.fillRect(0, 0, px, px);
        ctx.fillStyle = '#000000';
        for (let r = 0; r < n; r++) {
            for (let c = 0; c < n; c++) {
                if (matrix[r][c]) {
                    ctx.fillRect((c + quiet) * scale, (r + quiet) * scale, scale, scale);
                }
            }
        }
    }, [matrix, size]);

    if (!matrix) {
        return (
            <div
                style={{
                    width: size,
                    height: size,
                    display: 'flex',
                    alignItems: 'center',
                    justifyContent: 'center',
                    textAlign: 'center',
                    background: 'rgba(255,255,255,0.04)',
                    border: '1px dashed var(--panel-border)',
                    borderRadius: 'var(--radius-md)',
                    color: 'var(--text-muted)',
                    fontSize: '12px',
                    padding: '12px',
                }}
            >
                QR unavailable — paste the setup key below into your authenticator.
            </div>
        );
    }

    return (
        <canvas
            ref={canvasRef}
            style={{ width: size, height: size, borderRadius: 'var(--radius-md)', background: '#fff' }}
            aria-label="TOTP provisioning QR code"
        />
    );
}

// ---------------------------------------------------------------------------
// A copyable inline text chip.
// ---------------------------------------------------------------------------
function CopyChip({ value, mono = true }: { value: string; mono?: boolean }) {
    const [copied, setCopied] = useState(false);
    const onCopy = async () => {
        try {
            await navigator.clipboard.writeText(value);
            setCopied(true);
            window.setTimeout(() => setCopied(false), 2000);
        } catch {
            /* clipboard unavailable */
        }
    };
    return (
        <div style={{ display: 'flex', gap: '8px', alignItems: 'stretch' }}>
            <input
                className="form-control"
                value={value}
                readOnly
                onFocus={(e) => e.currentTarget.select()}
                style={{
                    flex: 1,
                    fontFamily: mono ? "'SFMono-Regular', Consolas, Menlo, monospace" : undefined,
                    fontSize: '13px',
                }}
            />
            <button
                type="button"
                className="btn btn-secondary"
                style={{ padding: '0 14px' }}
                onClick={onCopy}
                aria-label="Copy"
                title="Copy"
            >
                {copied ? <Check size={16} color="var(--success-color)" /> : <Copy size={16} />}
            </button>
        </div>
    );
}

