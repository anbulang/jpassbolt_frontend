        setMeLoading(true);
        setMeError(null);
        try {
            const user = await getMe();
            setMe(user);
        } catch (err: unknown) {
            setMeError(errMessage(err, 'Failed to load your account.'));
        } finally {
            setMeLoading(false);
        }
